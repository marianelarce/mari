    public void validar() {
        System.out.println("Entro a validar()");
        String correo = txtNombre.getText();
        String pasword = String.valueOf(txtPass.getPassword());
        String nom = txtNombre.getText();
        String rol = cbxRol.getSelectedItem().toString();
        if (!"".equals(correo) || !"".equals(pasword) || !"".equals(nom)) {
            lg.setNombre(nom);
            lg.setCorreo(correo);
            lg.setPass(pasword);
            lg.setRol(rol);
            login.Registrar(lg);
            frmLogin si = new frmLogin();
            si.setVisible(true);
            dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Error de Conexion");
        }
    }

